//
// write in I/O display space mc6845 barely used in this case (on and off not done)
// 
// 6845 default regs for 9837 (80x25)
// R00 : 114
// R01 :  80
// R02 :  76
// R03 :   7
// R04 :  26
// R05 :  10
// R06 :  25
// R07 :  25
// R08 :   0
// R09 :  14
// R10 :  76	cursor shape : start at 14d, blink a 1/32
// R11 :  13	end at 19d
// 
// R11 :  $0D
// R12 :  $30 display pos : $30D0 : both area displayed (3) with 2 lines of soft keys (D0)
// R13 :  $D0  ""
// R14 :  $30 cursor pos : $30CF
// R15 :  $CF  ""

BYTE Write_Display37(BYTE *a, WORD d, BYTE s)
{
	if (d < 0x4000) {					// write rom
		a--;
		#if defined DEBUG_DISPLAY37
			k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *a);
			OutputDebugString(buffer); buffer[0] = 0x00;
		#endif
		return BUS_OK;						// do nothing but should be allowed ...
	}

	switch (d) {
		case 0x4001:			// line mover status reg ??
			a--;
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
//			_ASSERT(0);
			return BUS_OK;					// do nothing but should be allowed 
			break;
		case 0x4008:			// line mover replacement rule (even byte)
			a--;
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			if (s == 1) {				// bus error if byte access
//				_ASSERT(0);
				return BUS_ERROR;
			}
			s--;
		case 0x4009:			// line mover replacement rule 
			Chipset.Display37.lm_repr = (*--a);
			Chipset.Display37.rule = rules + ((Chipset.Display37.lm_repr & 0x0F) << 2);	// put offset rule
			#if defined DEBUG_DISPLAY37GM
				if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X rep rule\n"), Chipset.Cpu.PC, d, s, Chipset.Display37.lm_repr);
				OutputDebugString(buffer); buffer[0] = 0x00;
				};
			#endif
			if (s == 1) return BUS_OK;
			else {
				_ASSERT(0);
				return BUS_ERROR;
			}
			break;
		case 0x400C:			// line mover window width (< 1024 ??)
			if (s == 2) {
				Chipset.Display37.lm_width = (*(--a) << 8);
				Chipset.Display37.lm_width |= *(--a);
				#if defined DEBUG_DISPLAY37GM
					if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
					k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %04X window width\n"), Chipset.Cpu.PC, d, s, Chipset.Display37.lm_width);
					OutputDebugString(buffer); buffer[0] = 0x00;
					}
				#endif
				Chipset.Display37.lm_sign = (Chipset.Display37.lm_width & 0x8000) ? 1 : 0;
				if (Chipset.Display37.lm_sign) Chipset.Display37.lm_width = (~Chipset.Display37.lm_width) + 1;
				return BUS_OK;
			} else {
				#if defined DEBUG_DISPLAY37GM
					if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
					k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %04X window width\n"), Chipset.Cpu.PC, d, s, Chipset.Display37.lm_width);
					OutputDebugString(buffer); buffer[0] = 0x00;
					}
				#endif
				_ASSERT(0);
				return BUS_ERROR;
			}
			break;

		case 0x6000:
			a--;
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			if (s == 1) {
				_ASSERT(0);
				return BUS_ERROR;
			}
			s--;
		case 0x6001:					// 6845 regs
			#if defined DEBUG_DISPLAY37G
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X reg\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			Chipset.Display37.reg = *(--a);
			if (s == 1) return BUS_OK;
			else {
				_ASSERT(0);
				return BUS_ERROR;
			}
			break;
		case 0x6002:
			a--;
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			if (s == 1) {
				_ASSERT(0);
				return BUS_ERROR;
			}
			s--;
		case 0x6003:					// 6845 regs
			#if defined DEBUG_DISPLAY37G
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X reg\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			Chipset.Display37.regs[Chipset.Display37.reg] = *(--a);
			if (s == 1) return BUS_OK;
			else {
				_ASSERT(0);
				return BUS_ERROR;
			}
			break;
		default:						// elsewhere is ok ... ???
			while (s) {
				#if defined DEBUG_DISPLAY37G
					k = wsprintf(buffer,_T("%06X: DISPLAY37 : Write at %04X(%d) -> %02X\n"), Chipset.Cpu.PC, d, s, *(--a));
					OutputDebugString(buffer); buffer[0] = 0x00;
				#endif
			s--;
			}
			return BUS_OK;
		break;
	}
	return BUS_ERROR;
}

//
// read in I/O display space
//
BYTE Read_Display37(BYTE *a, WORD d, BYTE s)
{
	if (s == 1) {													// byte access
		if ((d < 0x4000) && ((d & 0x0001) == 0x0001)) {							// read rom on odd bytes
			*(--a) = rom[d >> 1];
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			return BUS_OK;
		} else if ((d < 0x4000) && ((d & 0x0001) == 0x0000)) {					// read rom on even bytes
			*(--a) = 0xFF;
			return BUS_OK;
		}  else  if (d == 0x4001) {						// line mover status
			*(--a) = Chipset.Display37.lm_st | 0x43;							// 0x43 is ok (by single stepping basic), other to try (no docs for 0x4)
			#if defined DEBUG_DISPLAY37GM
				if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X lm status\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
				}
			#endif
			return BUS_OK;
		}  else if (d == 0x6003) {						// 8545 regs
			*(--a) = Chipset.Display37.regs[Chipset.Display37.reg];
			#if defined DEBUG_DISPLAY37G
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read reg %02d = %02X\n"), Chipset.Cpu.PC, Chipset.Display37.reg, Chipset.Display37.regs[Chipset.Display37.reg]);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			return BUS_OK;
		}
	} else if (s == 2) {											// word access
		if (d < 0x4000) {														// read rom
			*(--a) = 0xFF;															// even byte
			*(--a) = rom[d >> 1];													// odd byte
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			return BUS_OK;
		} else if (d == 0x4000) {			// line mover status
			*(--a) = 0x00;
			*(--a) = Chipset.Display37.lm_st | 0x43;							// 0x43 is ok (by single stepping basic), other to try (no docs for 0x4)
			#if defined DEBUG_DISPLAY37GM
				if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X status\n"), Chipset.Cpu.PC, d, s, Chipset.Display37.lm_width);
				OutputDebugString(buffer); buffer[0] = 0x00;
				}
			#endif
			return BUS_OK;
		} else if (d == 0x400C) {			// line mover window width (< 1024 ??)
			*(--a) = (BYTE) (Chipset.Display37.lm_width >> 8);
			*(--a) = (BYTE) (Chipset.Display37.lm_width & 0xFF);
			#if defined DEBUG_DISPLAY37GM
				if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X window width\n"), Chipset.Cpu.PC, d, s, Chipset.Display37.lm_width);
				OutputDebugString(buffer); buffer[0] = 0x00;
				}
			#endif
			return BUS_OK;
		} else {
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			_ASSERT(0);
			return BUS_ERROR;
		}
	} else {
		#if defined DEBUG_DISPLAY37
			k = wsprintf(buffer,_T("%06X: DISPLAY37 : Read at %04X(%d) = %04X\n"), Chipset.Cpu.PC, d, s, *a);
			OutputDebugString(buffer); buffer[0] = 0x00;
		#endif
		_ASSERT(0);
	}
	return BUS_ERROR;
}

//
// read in graph mem
// line mover mode don't change anything ... strange (no docs, just some hints)
//
BYTE Read_Graph37(BYTE *a, DWORD d, BYTE s)
{
	if (Chipset.Display37.lm_repr & 0x80) {					// line mover mode ? no
		Chipset.Display37.lm_src = d;							// set source
		while (s) {												// read mem (one bit per byte)
			*(--a) = (pbyGraph[d]) ? 1 : 0;							
			d++;
			s--;
		}
		#if defined DEBUG_DISPLAY37GM
			if ((Chipset.Cpu.PC > 0xC000) || bDebugOn) {
			k = wsprintf(buffer,_T("%06X: GRAPH37 : Read %06X(%d) = %02X line mover source\n"), Chipset.Cpu.PC, d, s, *a);
			OutputDebugString(buffer); buffer[0] = 0x00;
			}
		#endif
	} else {												// line mover mode ? yes
		Chipset.Display37.lm_src = d;							// set source
		while (s) {												// read mem (one bit per byte)
			*(--a) = (pbyGraph[d]) ? 1 : 0;
			#if defined DEBUG_DISPLAY37
				k = wsprintf(buffer,_T("%06X: GRAPH37 : Read %06X(%d) = %02X\n"), Chipset.Cpu.PC, d, s, *a);
				OutputDebugString(buffer); buffer[0] = 0x00;
			#endif
			d++;
			s--;
		}
	}
	return BUS_OK;
}

