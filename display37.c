//
// copy graph mem into graph bitmap (from a reloaded system)
//
VOID Reload_Graph37(VOID)
{
	DWORD i;
	LPBYTE adr;

	#if defined DEBUG_DISPLAY37S
		wsprintf(buffer,_T("%06X: Update Main Display, %d\n"), Chipset.Cpu.PC, bForce);
		OutputDebugString(buffer);
	#endif

	adr = pbyGraph; 

	for (i = 0; i < 131072; i++) {
		*adr++ = (Chipset.Display37.graph[i] & 0x80) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x40) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x20) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x10) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x08) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x04) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x02) ? 1 : 0;
		*adr++ = (Chipset.Display37.graph[i] & 0x01) ? 1 : 0;
	}

	Chipset.Display37.g_xmax = Chipset.Display37.graph_width;
	Chipset.Display37.g_xmin = 0;
	Chipset.Display37.g_ymax = Chipset.Display37.graph_height;
	Chipset.Display37.g_ymin = 0;
}


VOID Refresh_Display37(BOOL bforce)
{
	if (bforce) {
		Chipset.Display37.g_xmax = Chipset.Display37.graph_width;
		Chipset.Display37.g_xmin = 0;
		Chipset.Display37.g_ymax = Chipset.Display37.graph_height;
		Chipset.Display37.g_ymin = 0;
	}
	if (Chipset.Display37.g_xmax > Chipset.Display37.g_xmin) {	// something to draw in graph part
		BitBlt(hWindowDC, Chipset.Display37.g_xmin, Chipset.Display37.g_ymin, Chipset.Display37.g_xmax-Chipset.Display37.g_xmin, Chipset.Display37.g_ymax-Chipset.Display37.g_ymin, hGraphDC, Chipset.Display37.g_xmin, Chipset.Display37.g_ymin, SRCCOPY);
		GdiFlush();
		Chipset.Display37.g_xmin = Chipset.Display37.graph_width;
		Chipset.Display37.g_xmax = 0;
		Chipset.Display37.g_ymin = Chipset.Display37.graph_height;
		Chipset.Display37.g_ymax = 0;
	}
}

// line mover part	some bits at a time ...

VOID do_display37(VOID) 
{
	BYTE i=0;
	WORD x0, y0;
	BYTE im = lm_max[wRealSpeed];		// number of bits to do this call

	do {
		Chipset.Display37.dest &= 0x000FFFFF;
		Chipset.Display37.src &= 0x000FFFFF;
		// apply the rule 
		pbyGraph[Chipset.Display37.dest] = Chipset.Display37.rule[(pbyGraph[Chipset.Display37.src] << 1) | pbyGraph[Chipset.Display37.dest]]; 
		x0 = (WORD) (Chipset.Display37.dest & 0x3FF);
		y0 = (WORD) (Chipset.Display37.dest >> 10);
		if (y0 < 768) {								// visible ?
			if (x0+1 > Chipset.Display37.g_xmax)		// adjust rectangle
				Chipset.Display37.g_xmax = x0+1;
			if (x0 < Chipset.Display37.g_xmin) 
				Chipset.Display37.g_xmin = x0;
			if (y0+1 > Chipset.Display37.g_ymax) 
				Chipset.Display37.g_ymax = y0+1;
			if (y0 < Chipset.Display37.g_ymin) 
				Chipset.Display37.g_ymin = y0;
		}
		if (Chipset.Display37.lm_sign) {
			Chipset.Display37.dest--;
			Chipset.Display37.src--;
		} else {
			Chipset.Display37.dest++;
			Chipset.Display37.src++;
		}
		Chipset.Display37.count--;
		Chipset.Display37.count &= 0x03FF;		// modulo 1024
		i++;
	} while ((Chipset.Display37.count != 0x0000) && (i != im));	// end ?
	if (Chipset.Display37.count == 0x0000) {
		Chipset.Display37.lm_st = 0x80;						// move done
		Chipset.Display37.lm_go = 0;
	}
}

//
// do VBL
//
VOID do_display_timers37(VOID)							
{
	Chipset.Display37.cycles += Chipset.dcycles;
	if (Chipset.Display37.cycles >= dwVsyncRef) {		// one vsync
		Chipset.Display37.cycles -= dwVsyncRef;
		Chipset.Display37.cnt ++;
		Chipset.Display37.cnt &= 0x3F;
		Refresh_Display37(FALSE);						// just rects
		UpdateAnnunciators(FALSE);
	}
}
